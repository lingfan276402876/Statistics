	public GeneralReport(ReportDto reportDto,List<View> viewNames ) {
		DynamicCreateObject.setFieldValue(this,"stat_time",reportDto.getStat_tiem());
		a: for (int i = 0; i < viewNames.size(); i++) {
			View view = viewNames.get(i);
			for (uc.game.statistics.view.ReportDto.KpiView kpiView : reportDto.getViews()) {
				if(kpiView.getViewName().equals(view.getViewName()))
				{
					String value = kpiView.getViewValue()==null?"0":kpiView.getViewValue();
					if(StringUtils.isNotBlank(view.getAggregate()))
					{
						value = view.calculate(reportDto);
						DynamicCreateObject.setFieldValue(this,"filed"+(i+1),value);
					}else
					{
						DynamicCreateObject.setFieldValue(this,"filed"+(i+1),value);
					}
					continue a;
				}
			}
			if(view.getFieldName().equalsIgnoreCase("stat_time"))
			{
				continue;
			}
			DynamicCreateObject.setFieldValue(this,"filed"+(i+1),"0");
		}
	}

